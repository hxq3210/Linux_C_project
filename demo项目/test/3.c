#include<stdio.h>

int main(int argc, char *argv[])
{
	int N,i,j;
	printf("请输入N的值：\n");
	scanf("%d",&N);
	for(i=0;i<N;i++)
	{
		for(j=0;j<N;j++)
		{
			printf(" %d  ",N);
		}
		printf("\n");
	}
    return 0;
}
